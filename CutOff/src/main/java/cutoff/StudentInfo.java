package cutoff;

public class StudentInfo {

	private String rollNumber;
	private String name;
	private int maths;
	private int chemistry;
	private int physics;
	private int biology;
	private int botany;
	private int zoology;
	private int computerScience;
	private double cutOffMark;

	public String getRollNumber() {
		return rollNumber;
	}

	public void setRollNumber(String rollNumber) {
		this.rollNumber = rollNumber;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getMaths() {
		return maths;
	}

	public void setMaths(int maths) {
		this.maths = maths;
	}

	public int getChemistry() {
		return chemistry;
	}

	public void setChemistry(int chemistry) {
		this.chemistry = chemistry;
	}

	public int getPhysics() {
		return physics;
	}

	public void setPhysics(int physics) {
		this.physics = physics;
	}

	public int getBiology() {
		return biology;
	}

	public void setBiology(int biology) {
		this.biology = biology;
	}

	public int getBotany() {
		return botany;
	}

	public void setBotany(int botany) {
		this.botany = botany;
	}

	public int getZoology() {
		return zoology;
	}

	public void setZoology(int zoology) {
		this.zoology = zoology;
	}

	public int getComputerScience() {
		return computerScience;
	}

	public void setComputerScience(int computerScience) {
		this.computerScience = computerScience;
	}

	public double getCutOffMark() {
		return cutOffMark;
	}

	public void setCutOffMark(double cutOffMark) {
		this.cutOffMark = cutOffMark;
	}

}
